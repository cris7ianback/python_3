# Operadores lógicos
# los operadores lógicos siguen las reglas de la lógica proposicional

# AND
print(True and False)
print(True and True)
print(False and False)

# OR
print(True or False)
print(True or True)
print(True or False)

# NOT ****
print(not True)
print(not False)

# Operadores de comparación
# [ == ; != ; < ; > ; >= ; <=]

# para verificar Igualdad se usa ==
print("¿Se cumple que 2 == 2\n?", 2 == 3)

# para probar desigualdad se usa !=
print("¿Se cumple que 2 != 2\n?", 2 != 2)

# Para comparar magnitudes usamos <, >, >=, <=

print("¿Se cumple que 2>3? \n", 2 > 3)
print("¿Se cumple que 2<3? \n", 2 < 3)
print("¿Se cumple que 2<= 3? \n", 2 <= 2)
print("¿Se cumple que 2 >=3?\n", 2 >= 3)


# Los operadores básicos de comparación (< , <=, >, >=, ==, !=) también aplican a las cadena de caracteres. Para compararlas siguen el estandar ASCCI
print("c" > "d", "\n", 'c' > 'b')
# Si Empezamos a contar desde la A: la C tiene un valor de 3 mientras que la P tiene un valor de 16
print("casa" > "perro")
